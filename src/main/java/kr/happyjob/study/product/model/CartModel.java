package kr.happyjob.study.product.model;

public class CartModel {
	private String loginID;
	private String item_code;
	private int obtain_count;
	private String receive_date;

	// 구매 시 사용
	private int cust_id;

	public String toString() {
		return "[loginID : " + loginID + " item_code : " + item_code + 
		" obtain_count : " + obtain_count + " receive_date : " + receive_date +
		" cust_id : " + cust_id + "]";
	}

	public String getLoginID() {
		return loginID;
	}

	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}

	public String getItem_code() {
		return item_code;
	}

	public void setItem_code(String item_code) {
		this.item_code = item_code;
	}

	public int getObtain_count() {
		return obtain_count;
	}

	public void setObtain_count(int obtain_count) {
		this.obtain_count = obtain_count;
	}

	public String getReceive_date() {
		return receive_date;
	}

	public void setReceive_date(String receive_date) {
		this.receive_date = receive_date;
	}

	public int getCust_id() {
		return cust_id;
	}

	public void setCust_id(int cust_id) {
		this.cust_id = cust_id;
	}
}
