package kr.happyjob.study.direction.model;


//재고이력 테이블
public class TB_inventory_history {
	
	private String storage_code;	//FK : 창고코드
	private String model_num;		//FK : 모델번호
	private String item_code;		//FK : 품목코드
	private String equipment_num;	//장비번호
	private String storage_type;	//유형
	private String storage_date;	//날짜
	private int count;				//수량
	public String getStorage_code() {
		return storage_code;
	}
	public void setStorage_code(String storage_code) {
		this.storage_code = storage_code;
	}
	public String getModel_num() {
		return model_num;
	}
	public void setModel_num(String model_num) {
		this.model_num = model_num;
	}
	public String getItem_code() {
		return item_code;
	}
	public void setItem_code(String item_code) {
		this.item_code = item_code;
	}
	public String getEquipment_num() {
		return equipment_num;
	}
	public void setEquipment_num(String equipment_num) {
		this.equipment_num = equipment_num;
	}
	public String getStorage_type() {
		return storage_type;
	}
	public void setStorage_type(String storage_type) {
		this.storage_type = storage_type;
	}
	public String getStorage_date() {
		return storage_date;
	}
	public void setStorage_date(String storage_date) {
		this.storage_date = storage_date;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}

	
	
}
